package org.example;

public class Main {
    public static void main(String[] args) {

        //declaración de un dato de tipo byte
        byte edad = 21;
        System.out.println(edad);

        //declaración de una variable de tipo short
        short cantidadManzanas = 204;
        System.out.println(cantidadManzanas);

        //declaración de una variable de tipo int
        int cabras = 300;
        System.out.println(cabras);

        //declaración de una variable de tipo long
        long alcachofas = 4000L;
        System.out.println(alcachofas);

        //declaración de una variable de tipo char
        char caracter1 = 'A';
        System.out.println(caracter1);

        char caracter2 = 75;
        System.out.println(caracter2);

        //declaración de una variable de tipo boolean
        boolean esMayor = true;
        System.out.println(esMayor);

        boolean esMenor = false;
        System.out.println(esMenor);

        //declaración de una variable de tipo float
        float precio = 123.325f;
        System.out.println(precio);

        //declaración de una variable de tipo double
        double precio2 = 128.415684653d;
        System.out.println(precio2);

        //declaración de una variable de tipo String
        String nombre = "Manuel";
        System.out.println(nombre);
    }
}